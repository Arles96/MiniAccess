    private void jButtonAgregarCampoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarCampoActionPerformed
        DefaultTableModel model = (DefaultTableModel) tablaCampos.getModel();
        String nombre, tipo;
        int size;
        nombre = tfNombreCampo.getText();
        if (nombre.equals("")) {
            JOptionPane.showMessageDialog(this, "El nombre esta vacio");
            return;
        }
        tipo = jcTipoCampo.getSelectedItem().toString();
        try {
            size = Integer.parseInt(tfTamanioCampo.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error en la casilla tama√±o.");
            return;
        }
        Campos campos = new Campos(nombre, size, tipo);
        Object rowData [] = {nombre, tipo, size};
        model.addRow(rowData);
        modelo.agregarCampo(campos);
    }//GEN-LAST:event_jButtonAgregarCampoActionPerformed

    private void jButtonEliminarCampoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarCampoActionPerformed
        if (tablaCampos.getSelectedRow()>-1) {
            modelo.eliminarCampo(tablaCampos.getSelectedRow());
            DefaultTableModel model = (DefaultTableModel) tablaCampos.getModel();
            model.removeRow(tablaCampos.getSelectedRow());
            JOptionPane.showMessageDialog(this, "Se ha eliminado el campo");
        }
    }//GEN-LAST:event_jButtonEliminarCampoActionPerformed

