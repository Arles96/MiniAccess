    private void jButtonAgregarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarRegistroActionPerformed
        try {
            Registro registro = new Registro();
            ArrayList<Campos> campos = (ArrayList<Campos>)modelo.getCampos().clone();
            registro.setCampos(campos);
            for (int i = 0; i < model_principal.getColumnCount(); i++) {
                System.out.println(model_principal.getValueAt(model_principal.getRowCount()-1, i).toString());
                registro.getCampo(i).setContent(model_principal.getValueAt(model_principal.getRowCount()-1, i).toString());
            }
            Object rowData [] = {};
            model_principal.addRow(rowData);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al agregar el registro");
            for (int i = 0; i < model_principal.getColumnCount(); i++) {
                model_principal.setValueAt("", model_principal.getRowCount()-1, i);
            }
        }
    }//GEN-LAST:event_jButtonAgregarRegistroActionPerformed

