    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        int seleccion = JOptionPane.showConfirmDialog(this, "Desea guardar los cambios.");
        if (JOptionPane.OK_OPTION==seleccion) {
            JOptionPane.showMessageDialog(this, "Se ha guardado los cambios.");
            NuevoCampo.setVisible(false);
            tabla_principal.setModel(model_principal);
            modelo.getCampos().forEach((campo)->{
                model_principal.addColumn(campo.getNombre());
            });
            Object rowData[] = {};
            model_principal.addRow(rowData);
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

